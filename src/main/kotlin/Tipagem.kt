fun main(args: Array<String>) {
    var variavelString : String = "Essa é uma string"
    var variavelInt : Int = 1 // Esse é um inteiro
    var variavelDouble : Double = 1.0 // Esse é um double
    var variavelBoolean : Boolean = true // Esse é um boolean
    var variavelChar : Char = 'a' // Esse é um char
    var variavelFloat : Float = 1.0f // Esse é um float
    var variavelLong : Long = 1L // Esse é um long
    var variavelShort : Short = 1 // Esse é um short
    var variavelByte : Byte = 1 // Esse é um byte
    var variavelAny : Any = 1 // Esse é um any


    println("Escrevendo a string $variavelString")
    println("Escrevendo o inteiro $variavelInt")
    println("Escrevendo o double $variavelDouble")
    println("Escrevendo o boolean $variavelBoolean")
    println("Escrevendo o char $variavelChar")
    println("Escrevendo o float $variavelFloat")
    println("Escrevendo o long $variavelLong")
    println("Escrevendo o short $variavelShort")
    println("Escrevendo o byte $variavelByte")
    println("Escrevendo o any $variavelAny")


}