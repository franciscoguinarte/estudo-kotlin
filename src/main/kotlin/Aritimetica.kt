fun main(args: Array<String>) {
    var soma = 1 + 2
    var subtracao = 1 - 2
    var multiplicacao = 1.0 * 2
    var divisao = (1.0 / 2.0)
    var modulo = 1 % 2

    println("Soma: $soma")
    println("Subtracao: $subtracao")
    println("Multiplicacao: $multiplicacao")
    println("Divisao: $divisao")
    println("Modulo: $modulo")
    println("Incremento: ${++soma}")
    println("Decremento: ${--soma}")


}